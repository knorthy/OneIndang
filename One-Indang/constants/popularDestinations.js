// Helper to get destinations by category
export const getDestinationsByCategory = (category) => {
  if (category === 'All') return popularDestinations;
  return popularDestinations.filter(d => d.category === category);
};

// Helper to search destinations
export const searchDestinations = (query) => {
  const lowerQuery = query.toLowerCase();
  return popularDestinations.filter(d =>
    d.name.toLowerCase().includes(lowerQuery) ||
    d.barangay.toLowerCase().includes(lowerQuery) ||
    d.category.toLowerCase().includes(lowerQuery)
  );
};
